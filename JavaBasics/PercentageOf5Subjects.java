package JavaBasics;

public class PercentageOf5Subjects {

	public static void main(String[] args) {
		// Calculate percentage out of 60
		double maths = 56;
		double eng = 55;
		double eco = 54;
		double hin = 45;
		double urd = 57;
		
		System.out.println((maths + eng + eco + hin + urd)/(300)*100);

	}

}
